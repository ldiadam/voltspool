func TestGenerateGenericReceipt(t *testing.T) {
	gr := GenericReceipt{
		Width: 32,
		Lines: []GenericLine{
			{Type: "text", Text: "GENERIC STORE", Align: "CENTER", Bold: true, Size: "DOUBLE_HW"},
			{Type: "divider", Char: "-"},
			{Type: "columns", Left: "Item 1", Right: "Rp 15.000"},
			{Type: "columns", Left: "Item 2", Right: "Rp 25.000"},
			{Type: "divider", Char: "="},
			{Type: "columns", Left: "TOTAL", Right: "Rp 40.000", Bold: true},
			{Type: "cut"},
		},
	}

	bytes, err := GenerateGenericReceipt(gr)
	if err != nil {
		t.Fatalf("GenerateGenericReceipt returned unexpected error: %v", err)
	}

	if len(bytes) == 0 {
		t.Fatal("GenerateGenericReceipt returned empty byte slice")
	}

	strBytes := string(bytes)
	if !strings.Contains(strBytes, "GENERIC STORE") {
		t.Error("Generated bytes missing generic header")
	}
	if !strings.Contains(strBytes, "Item 1") {
		t.Error("Generated bytes missing generic Item 1")
	}
	if !strings.Contains(strBytes, "TOTAL") {
		t.Error("Generated bytes missing generic TOTAL")
	}
	if !strings.Contains(strBytes, strings.Repeat("=", 32)) {
		t.Error("Generated bytes missing generic double divider")
	}
}

