func TestFormatRupiah(t *testing.T) {
	tests := []struct {
		input    int
		expected string
	}{
		{0, "Rp 0"},
		{500, "Rp 500"},
		{1000, "Rp 1.000"},
		{15000, "Rp 15.000"},
		{250000, "Rp 250.000"},
		{1000000, "Rp 1.000.000"},
		{-5000, "-Rp 5.000"},
	}

	for _, tc := range tests {
		actual := FormatRupiah(tc.input)
		if actual != tc.expected {
			t.Errorf("FormatRupiah(%d) = %q, expected %q", tc.input, actual, tc.expected)
		}
	}
}

func TestWrapText(t *testing.T) {
	tests := []struct {
		text     string
		width    int
		expected []string
	}{
		{"Short text", 32, []string{"Short text"}},
		{"", 32, []string{""}},
		{
			"Super Premium Delicious Roasted Coffee Beans Medium Blend",
			32,
			[]string{
				"Super Premium Delicious Roasted",
				"Coffee Beans Medium Blend",
			},
		},
		{
			"Supercalifragilisticexpialidocious", // 34 chars
			10,
			[]string{
				"Supercalif",
				"ragilistic",
				"expialidoc",
				"ious",
			},
		},
	}

	for _, tc := range tests {
		actual := WrapText(tc.text, tc.width)
		if len(actual) != len(tc.expected) {
			t.Fatalf("WrapText(%q, %d) returned %d lines, expected %d. Got: %v", tc.text, tc.width, len(actual), len(tc.expected), actual)
		}
		for i := range actual {
			if actual[i] != tc.expected[i] {
				t.Errorf("WrapText(%q, %d) line %d = %q, expected %q", tc.text, tc.width, i, actual[i], tc.expected[i])
			}
		}
	}
}

